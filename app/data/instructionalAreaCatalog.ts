export function getInstructionalAreaCatalog(category: string): InstructionalArea[] {
  const normalizedCategory = category === 'ENTREPRENEUR' ? 'ENTREPRENEURSHIP' : category
  const rows = CATALOG[normalizedCategory as CatalogCategory] || CATALOG.MANAGEMENT

  return rows.map(([name, code, piCount]) => ({
    name,
    code,
    piCount,
    description: `Performance indicators related to ${name.toLowerCase()}`,
  }))
}
